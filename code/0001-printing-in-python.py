# Method 1: Using simple print function

print("Hello World")

# Method 2: Printing multiple values

name = "Himanshu"
age = 27

print("Name:", name, "Age:", age)

# Method 3: using sep parameter to separate multiple values

print("python", "is", "easy", sep="-")
print("python", "is", "easy", sep="_")

# Method 4: Using end parameter

print("Hello", end=" ")
print("World")

# Method 5: Using Formatted String

print(f"Name: {name}, Age: {age}")

# Method 6: Using str.format

print("Name: {0}, Age: {1}".format(name, age))
